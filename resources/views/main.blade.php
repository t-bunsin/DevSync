    <script>
        document.addEventListener('DOMContentLoaded', function () {
            const jobs = @json($jobs);
            const jobsById = Object.fromEntries(jobs.map((job) => [job.id, job]));

            const list = document.getElementById('job-card-list');
            const cards = Array.from(list.querySelectorAll('.jf-job-card'));
            const tabs = Array.from(document.querySelectorAll('.jf-tab'));
            const jobCount = document.getElementById('job-count');
            const sortSelect = document.getElementById('job-sort-select');
            const titleInput = document.getElementById('job-search-input');
            const locationInput = document.getElementById('job-location-input');
            const searchButton = document.getElementById('job-search-button');
            const quickApply = document.getElementById('detail-quick-apply');

            const detailBadge = document.getElementById('detail-badge');
            const detailTitle = document.getElementById('detail-title');
            const detailCompany = document.getElementById('detail-company');
            const detailLocation = document.getElementById('detail-location');
            const detailSalary = document.getElementById('detail-salary');
            const detailPosted = document.getElementById('detail-posted');
            const detailApplicants = document.getElementById('detail-applicants');
            const detailSectionTitle = document.getElementById('detail-section-title');
            const detailSectionBody = document.getElementById('detail-section-body');
            const detailListTitle = document.getElementById('detail-list-title');
            const detailList = document.getElementById('detail-list');
            const detailFacts = document.getElementById('detail-facts');
            const detailQuickTitle = document.getElementById('detail-quick-title');
            const detailQuickText = document.getElementById('detail-quick-text');

            let activeJobId = '{{ $selectedJob['id'] }}';
            let activeTab = 'description';

            function normalize(text) {
                return text.toLowerCase().replace(/[^a-z\s]/g, ' ').replace(/\s+/g, ' ').trim();
            }

            function hasActiveFilters() {
                return titleInput.value.trim() !== '' || normalize(locationInput.value) !== 'phnom penh';
            }

            function updateResultCount() {
                const visibleCards = cards.filter((card) => !card.hidden);
                jobCount.textContent = hasActiveFilters() ? visibleCards.length.toLocaleString() : '1,247';

                if (!visibleCards.some((card) => card.dataset.jobId === activeJobId) && visibleCards[0]) {
                    renderJob(visibleCards[0].dataset.jobId);
                }
            }

            function renderFacts(job) {
                detailFacts.innerHTML = job.detail_items.map((item) => `
                    <div class="jf-fact">
                        <span>${item.label}</span>
                        <strong>${item.value}</strong>
                    </div>
                `).join('');
            }

            function renderTab(job) {
                const panel = job.tabs[activeTab];

                detailSectionTitle.textContent = panel.title;
                detailSectionBody.textContent = panel.body;
                detailListTitle.textContent = panel.list_title;
                detailList.innerHTML = panel.list.map((item) => `<li>${item}</li>`).join('');

                const showQuickApply = activeTab === 'description';
                quickApply.hidden = !showQuickApply;
            }

            function renderJob(jobId) {
                const job = jobsById[jobId];
                if (!job) {
                    return;
                }

                activeJobId = jobId;

                cards.forEach((card) => {
                    const isActive = card.dataset.jobId === jobId;
                    card.classList.toggle('is-active', isActive);
                    card.setAttribute('aria-pressed', isActive ? 'true' : 'false');
                });

                detailBadge.textContent = job.featured ? 'FEATURED' : 'OPEN ROLE';
                detailTitle.textContent = job.title;
                detailCompany.textContent = job.company;
                detailLocation.textContent = job.location;
                detailSalary.textContent = job.salary;
                detailPosted.textContent = job.posted;
                detailApplicants.textContent = job.applicants;
                detailQuickTitle.textContent = job.quick_apply.title;
                detailQuickText.textContent = job.quick_apply.text;

                renderFacts(job);
                renderTab(job);
            }

            function filterJobs() {
                const titleQuery = normalize(titleInput.value);
                const locationQuery = normalize(locationInput.value);

                cards.forEach((card) => {
                    const matchesTitle = !titleQuery ||
                        normalize(card.dataset.title).includes(titleQuery) ||
                        normalize(card.dataset.company).includes(titleQuery);
                    const matchesLocation = !locationQuery || normalize(card.dataset.location).includes(locationQuery);

                    card.hidden = !(matchesTitle && matchesLocation);
                });

                updateResultCount();
            }

            function sortJobs() {
                const sortedCards = [...cards].sort((left, right) => {
                    if (sortSelect.value === 'salary') {
                        return Number(right.dataset.salaryRank) - Number(left.dataset.salaryRank);
                    }

                    if (sortSelect.value === 'featured') {
                        return Number(right.dataset.featured) - Number(left.dataset.featured);
                    }

                    return Number(left.dataset.postedDays) - Number(right.dataset.postedDays);
                });

                sortedCards.forEach((card) => list.appendChild(card));
            }

            cards.forEach((card) => {
                const activate = () => renderJob(card.dataset.jobId);

                card.addEventListener('click', activate);
                card.addEventListener('keydown', (event) => {
                    if (event.key === 'Enter' || event.key === ' ') {
                        event.preventDefault();
                        activate();
                    }
                });
            });

            tabs.forEach((tab) => {
                tab.addEventListener('click', () => {
                    activeTab = tab.dataset.tab;

                    tabs.forEach((button) => {
                        const isActive = button === tab;
                        button.classList.toggle('is-active', isActive);
                        button.setAttribute('aria-selected', isActive ? 'true' : 'false');
                    });

                    renderTab(jobsById[activeJobId]);
                });
            });

            sortSelect.addEventListener('change', sortJobs);
            searchButton.addEventListener('click', filterJobs);
            titleInput.addEventListener('keydown', (event) => {
                if (event.key === 'Enter') {
                    filterJobs();
                }
            });
            locationInput.addEventListener('keydown', (event) => {
                if (event.key === 'Enter') {
                    filterJobs();
                }
            });

            renderJob(activeJobId);
            sortJobs();
            updateResultCount();
        });
    </script>
@endpush
